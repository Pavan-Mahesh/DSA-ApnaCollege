package Day007_Miscellaneous;

public class BitwiseOperator {
    public static void main(String[] args) {
        System.out.println("6 & 10: " + (6 & 10));
        System.out.println("6 | 10: " + (6 | 10));
        System.out.println("6 ^ 10: " + (6 ^ 10));
        System.out.println("10 << 2: " + (10 << 2));
        System.out.println("10 >> 1: " + (10 >> 1));
    }
}
